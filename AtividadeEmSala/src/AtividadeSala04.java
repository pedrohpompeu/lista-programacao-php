public class AtividadeSala04 {
    public static void main(String[] args){
        int x = 0;
        System.out.println("Valor de X +=1:"+ (x +=1));
        System.out.println("Valor de X ++:"+ ( x++));
        System.out.println("Valor de X -=2:"+ (x -=2));
        System.out.println("Valor de X:"+ x);
        System.out.println("Valor de X --:"+ (x--));
    }
}
